#　巡回符号の符号化プログラム（組織符号化）
def polymod(z,n,y,m): #多項式の剰余計算z(x)mod y(x)【多項式z(x)の次数n, 多項式y(x)の次数m】
    for i in range(n-m+1):
        z = z ^ ( y << (n-m-i)) if ( z >> (n-i) ) else z
    return z

def encoding(a): # 巡回符号の符号化　w(x)=a(x)x^m + c(x), c(x) = a(x)x^m mod g(x)
    return ((a << m)|polymod(a<<m,n-1,gx,m))

m = 2 # 生成多項式の次数 m = deg(g(x)) ※これが検査記号数(次数が2のため変更)
gx = 0b111 # 偶数パリティ符号の生成多項式 G(x) = x^2 +　x + 1 の２進表現(111)
k = 1 # 情報記号数(三回繰り返し符号用に修正)
n = 3 # 符号長(三回繰り返し符号用に修正)

for i in range(1<<(k)):
    print(format(i,'01b'),format(encoding(i),'03b')) #フォーマット修正
